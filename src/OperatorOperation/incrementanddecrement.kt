package OperatorOperation

fun main() {
    /**
     * incerement adalah menambahkan angka pada value sebelumnya
     * decrement adalah mengurangi angka pada value sebelumnya
     * ++ / --
     * */

    var angkaAwal = 10
    var angkaPertama = 10

    //decrement pada variable hasil
    val hasil:Int = angkaAwal --
    println(hasil)
    println(angkaAwal)

    //increment
    val  hasilIncrement:Int = angkaPertama ++
    println(angkaPertama)
    println(hasilIncrement)

}