package pemrogramanstruktur

fun main() {
    /**
     * apa itu kondisional operator
     * kondisional operator memiliki nilai return boolean
     * >
     * <
     * ==
     * !=
     * >=
     * <=
     * */

    var nilaiAwal = 7
    var nilaiKedua = 9

    println(nilaiAwal>nilaiKedua)
    println(nilaiAwal<nilaiKedua)
    println(nilaiAwal==nilaiKedua)
    println(nilaiAwal!=nilaiKedua)
    println(nilaiAwal>=nilaiKedua)
    println(nilaiAwal<=nilaiKedua)

}